var maxAreaOfIsland = function(grid) {
    
    if(grid.length === 0) return 0
    
    let max = 0
    const vstd = new Set()

    const hash = function(a,b) {
        return `${a}|${b}`
    }
    
    const dfs = function(m,n) {
        
        if(m < 0 || m > grid.length-1)   return 0
        if(n < 0 || n > grid[0].length-1)   return 0
        if(grid[m][n] === 0)            return 0

        if(vstd.has(hash(m,n))) return 0
        
        vstd.add(hash(m,n))
        
        return 1 + dfs(m+1,n) + dfs(m-1,n) + dfs(m,n+1) + dfs(m,n-1)
    }
    
    for(let i = 0; i < grid.length; i++) {
        for(let j = 0; j < grid[0].length; j++) {
            if(grid[i][j] !== 0 && !vstd.has(hash(i,j))) {
                max = Math.max(max, dfs(i,j))
            }
        }
    }
    
    return max
};

